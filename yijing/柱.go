//3、年命纳音为土
//
//正月寅木乘旺当令，克年命纳音之土也为不吉之命。同样年命受日、时纳音之克也为凶兆。
//
//4、时柱纳音克日柱纳音
//
//日柱纳音克月柱纳音，月柱纳音克年柱纳音，主损伤长辈尊人，预兆家门破败，对己不利。
//
//5、纳音五行有二柱或三柱克犯胎元纳音五行者
//
//也兆尊人早亡，家门破败和本人幼年孤贫。
//
//6、胎元纳音五行克年柱纳音五行
