// 纳音五行论命
//用八字纳音五行批命，以年上纳音代表命主，以年命与月柱、日柱和时柱结合起来批断命主富贵贫贱、吉凶祸福。
//
//1、年柱为命
//
//月、日、时柱纳音五行生年柱纳音五行，此为祖辈受生、主贵。同时预兆命主先贫后富，先贱后贵。
//
//2、年柱纳音被日柱纳音克
//
//年柱地支又被它柱地支刑，兆命主短寿夭贱。
