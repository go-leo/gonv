//主父母不全。
//
//7、四柱的纳音五行从上往下递次而克
//
//如年克月、月克日、日克时、为尊制卑、君制臣、顺理成章，为顺为吉，应称得天时。
//
//8、年柱纳音生月柱纳音
//
//月柱纳音生日柱纳音，日柱纳音生时柱纳音，从上而下递次相生者，贫贱之命。
//
//9、年柱纳音受日柱或时柱纳音相生者吉
//
//主后人进发，富贵。
//
//10、四柱纳音组成纯土、金、木、火、水者
//
//再当令乘旺，无制约，为纯旺太过，不为好命。
//
//11、年柱纳音，受日、时柱纳音之刑
//
//为下刑上，命主有违法遭刑和横死之祸。如庚戌金人，逢癸未日，纳音之木受金之克，对命主无大害，但最怕戌未相刑，命主受刑，刑出灾祸。
//
//12、年柱纳音被日、时柱纳音克
//
//同时地支又受刑，如年柱甲辰纳音为火，日柱或时柱为壬辰，纳音为水，水克火，二辰自刑，主此人头面眼目有疾，也易遭杀伤之祸。
//
//13、生日、生时在年柱纳音五行的长生之地
//
//自有天德相合，此即为八字逢生气加天德合，为长寿之命。年柱纳音五行为长生之地。
//
//14、年柱天干与日或时柱天干相合
//
//日或时支又为纳音败地（沐浴），主男女皆为道德败坏，不正之义。
//
//15、年柱纳音五行至旺地
//
//日柱或时柱天干克年柱天干，为官贵之命。如年柱庚申，纳音为木，木以同类为旺，所以，生在丙寅日寅为旺地，丙日干又克年柱庚，即为官贵之命。
//
//纳音本质上是按一定规律重新组合成的五行，中国古代预测法根源于阴阳五行，故，作为阴阳五行组合成的纳音必定对人的命运有一定影响。但是纳音有其局限性。
//
//六十甲子纳音表是中国古代的一种纪年方式，每六十年为一个甲子，共十二个甲子。而纳音是古代道教用以占卜的一种方法，分为六十个纳音，每个纳音都对应着一个动物、一个数字、一个方向和五行中的一个或两个。纳音五行口诀是指用简短的五言或七言诗句来记忆纳音的五行属性，这些口诀历史悠久，用以传承纳音知识。每个纳音的五行口诀都是精炼而富有意味的，栩栩如生，稍加揣摩，往往可以得出一些不同寻常的启示和感悟。例如，申的五行口诀为“金石之坚，水泄不通”，金代表了申的主属性，表示其坚固不拔；水代表了申的副属性，表示它能顺从流动。这些纳音五行口诀在中华文化中有着深远的影响，既可用于占卜预测，又可用于指导生活、修身养性。

